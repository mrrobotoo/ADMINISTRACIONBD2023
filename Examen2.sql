SELECT 
    a.ID_CURSO, 
    a.TITULO, 
    a.ID_PROFE AS ID_PROFE_CURSO,
    b.ID_PROFE, 
    b.NOMBRE AS NOMBRE_PROFESOR, 
    b.APELLIDOS AS APELLIDOS_PROFESOR, 
    b.F_NACIMIENTO
FROM 
    CURSOS a
FULL OUTER JOIN 
    PROFESORES b ON a.ID_CURSO = b.ID_PROFE;


